# 计算两数之和的函数
def add_numbers(a, b):
    """
    计算两个数之和的函数

    参数:
    a (int or float): 第一个数
    b (int or float): 第二个数

    返回:
    int or float: 两个数的和
    """
    return a + b

def jian_numbers(a, b):
    """
    计算两个数之差的函数

    参数:
    a (int or float): 第一个数
    b (int or float): 第二个数

    返回:
    int or float: 两个数的差
    """
    return a - b

def cheng_numbers(a, b):
    """
    计算两个数之差的函数

    参数:
    a (int or float): 第一个数
    b (int or float): 第二个数

    返回:
    int or float: 两个数的乘积
    """
    return a * b

